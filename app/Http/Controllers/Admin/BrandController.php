    {
        $sort_search = null;
        $brands = Brand::orderBy('id', 'desc');
        if ($request->has('search')){
            $sort_search = $request->search;
            $brands = $brands->where('name', 'like', '%'.$sort_search.'%');
        }
        $brands = $brands->paginate(10);
        return view('admin.brands.index', compact('brands', 'sort_search'));
    }

